
public class NumerosDel10al1 {

	public static void main(String[] args) {
		
		int numero, contador, resultado;
		
		numero = 10;
		
		contador = 0;
		
		while (contador<=9) {
			
			resultado= numero-contador;
			
			System.out.println(resultado);
			
			contador = contador + 1;
			
		}

	}

}
